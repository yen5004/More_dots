# Change dirs
alias ..="cd .."
alias cd..="cd .."
alias ...="cd ../../"
alias ....="cd ../../../"

# CD and ls
alias cdls="cd .. && ls"
alias cdla="cd .. && ls -la"
alias cdlh="cd .. && ls -lh"

# 4. More ls aliases
# Convenient shorthand for listing files, showing hidden files, and listing files in descending order of modification together with human readable file size.
alias ll='ls -l'
alias la='ls -Al'
alias lt='ls -ltrh'
